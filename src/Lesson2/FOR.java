package Lesson2;

//fori Цикл FOR
//инициализация выполняется 1 раз, проверяется условия, действие

public class FOR {
    public static void main(String[] args) {
        int n = 5;
        int res = 1;
        for (int i = 1; i <n ; i++) {
            res *= i;
        }
        System.out.println(res);
        //наибольший общий делитель двух чисел
    }
    }
